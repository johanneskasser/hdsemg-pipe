def plot_pipeline_overview(summary: PipelineSummary):
    """
    Create visual overview of completed pipeline steps.

    Returns matplotlib Figure or None if matplotlib not available.
    """
    try:
        import matplotlib.pyplot as plt
        import numpy as np
    except ImportError:
        print("matplotlib not available - cannot create plot")
        return None

    steps = summary.get_completed_steps()

    fig, ax = plt.subplots(figsize=(10, 6))

    # Prepare data
    step_nums = [s[0] for s in steps]
    step_names = [s[1] for s in steps]
    statuses = [s[2] for s in steps]

    # Color mapping
    colors = []
    for status in statuses:
        if status == "Completed":
            colors.append('#10b981')  # Green
        elif status == "Skipped":
            colors.append('#f59e0b')  # Orange
        else:
            colors.append('#6b7280')  # Gray

    # Create horizontal bar chart
    y_pos = np.arange(len(step_nums))
    ax.barh(y_pos, [1] * len(step_nums), color=colors, alpha=0.7)

    # Customize
    ax.set_yticks(y_pos)
    ax.set_yticklabels([f"Step {{n}}: {{name}}" for n, name in zip(step_nums, step_names)])
    ax.set_xlabel('Status')
    ax.set_title('HD-sEMG Pipeline Processing Steps')
    ax.set_xlim(0, 1)
    ax.set_xticks([])

    # Add legend
    from matplotlib.patches import Patch
    legend_elements = [
        Patch(facecolor='#10b981', label='Completed'),
        Patch(facecolor='#f59e0b', label='Skipped'),
        Patch(facecolor='#6b7280', label='Not Performed')
    ]
    ax.legend(handles=legend_elements, loc='lower right')

    plt.tight_layout()
    return fig


def plot_covisi_comparison(reader: MetadataReader):
    """
    Compare CoVISI values before and after cleaning.

    Returns matplotlib Figure or None if data not available.
    """
    try:
        import matplotlib.pyplot as plt
        import numpy as np
    except ImportError:
        print("matplotlib not available - cannot create plot")
        return None

    pre_report = reader.get_covisi_pre_filter()
    post_report = reader.get_covisi_post_validation()

    if not pre_report and not post_report:
        print("No CoVISI data available for comparison")
        return None

    fig, ax = plt.subplots(figsize=(10, 6))

    # Extract CoVISI values if available
    if pre_report and 'mu_covisi_values' in pre_report:
        mu_covisi = pre_report['mu_covisi_values']
        if mu_covisi:
            indices = [item[0] for item in mu_covisi]
            values = [item[1] for item in mu_covisi]
            ax.scatter(indices, values, alpha=0.6, label='Pre-Filter', s=50)

    # Add threshold line
    threshold = pre_report.get('threshold', 30.0) if pre_report else 30.0
    ax.axhline(y=threshold, color='r', linestyle='--', label=f'Threshold ({{threshold}}%)')

    ax.set_xlabel('Motor Unit Index')
    ax.set_ylabel('CoVISI (%)')
    ax.set_title('CoVISI Quality Assessment')
    ax.legend()
    ax.grid(True, alpha=0.3)

    plt.tight_layout()
    return fig


class ProtocolParser:
    """
    Parse hdsemg-trail protocol files (*_protokoll.txt) and map EMG file
    groups to experimental conditions based on the randomization order.

    Study design (crossover):
        Block1 (Baseline) -> Block2 (Pre) -> Training1 -> Block3 (Post-T1)
        -> Washout -> Block4 (Post-Washout) -> Training2 -> Block5 (Post-T2)
        -> Rest -> Block6 (Final)

    The randomization field determines which training mode (CON/EXZ) was first.
    """

    CONDITION_MAPS = {{
        'CON first': {{
            'Block1': {{'condition': 'Baseline', 'training_mode': None}},
            'Block2': {{'condition': 'Pre_Intervention', 'training_mode': None}},
            'Block3': {{'condition': 'Post_CON', 'training_mode': 'CON'}},
            'Block4': {{'condition': 'Post_Washout', 'training_mode': None}},
            'Block5': {{'condition': 'Post_EXZ', 'training_mode': 'EXZ'}},
            'Block6': {{'condition': 'Final', 'training_mode': None}},
        }},
        'EXZ first': {{
            'Block1': {{'condition': 'Baseline', 'training_mode': None}},
            'Block2': {{'condition': 'Pre_Intervention', 'training_mode': None}},
            'Block3': {{'condition': 'Post_EXZ', 'training_mode': 'EXZ'}},
            'Block4': {{'condition': 'Post_Washout', 'training_mode': None}},
            'Block5': {{'condition': 'Post_CON', 'training_mode': 'CON'}},
            'Block6': {{'condition': 'Final', 'training_mode': None}},
        }},
    }}

    # Ordered list of conditions for consistent output
    CONDITION_ORDER = [
        'Baseline', 'Pre_Intervention', 'Post_CON', 'Post_EXZ',
        'Post_Washout', 'Final',
    ]

    def __init__(self, paths: WorkfolderPaths):
        self.paths = paths
        self._protocol = None

    def find_protocol_file(self) -> Optional[Path]:
        """Find the protocol file in the workfolder root."""
        return self.paths.find_protocol_file()

    def parse(self) -> Optional[Dict]:
        """
        Parse the protocol file and return structured data.

        Returns dict with keys: 'file', 'metadata', 'steps'
        or None if no protocol file is found.
        """
        protocol_path = self.find_protocol_file()
        if not protocol_path:
            print("No protocol file (*_protokoll.txt) found in workfolder.")
            return None

        with open(protocol_path, 'r', encoding='utf-8') as f:
            content = f.read()

        self._protocol = {{
            'file': protocol_path.name,
            'metadata': self._parse_metadata(content),
            'steps': self._parse_steps(content),
        }}
        return self._protocol

    def _parse_metadata(self, content: str) -> Dict:
        """Extract key-value pairs from the Metadaten section."""
        metadata = {{}}
        in_metadata = False

        for line in content.split('\\n'):
            stripped = line.strip()

            if stripped.startswith('Metadaten:'):
                in_metadata = True
                continue
            if stripped.startswith('Schritte:') or stripped.startswith('====='):
                if in_metadata:
                    break

            if in_metadata and ':' in stripped:
                key, _, value = stripped.partition(':')
                key = key.strip()
                value = value.strip()
                if key and not key.startswith(('•', '▸', '✓', '⏱', '⌛', '📋')):
                    metadata[key] = value

        return metadata

    def _parse_steps(self, content: str) -> List[Dict]:
        """Extract structured data for each protocol step."""
        steps = []
        step_pattern = re.compile(
            r'\\[Schritt\\s+(\\d+)\\]\\s+(.+?)\\s+\\(([a-z_0-9]+)\\)'
        )

        current_step = None
        current_section = None  # 'inputs', 'notes', 'measurements'

        for line in content.split('\\n'):
            stripped = line.strip()

            # Match step header
            m = step_pattern.match(stripped)
            if m:
                if current_step:
                    steps.append(current_step)
                current_step = {{
                    'number': int(m.group(1)),
                    'name': m.group(2).strip(),
                    'step_id': m.group(3),
                    'inputs': {{}},
                    'notes': '',
                }}
                current_section = None
                continue

            if current_step is None:
                continue

            # Detect sections
            if stripped.startswith('Eingaben:'):
                current_section = 'inputs'
                continue
            elif stripped.startswith('Notizen:') or stripped.startswith('📝 Notizen:'):
                current_section = 'notes'
                continue
            elif stripped.startswith('Wiederholbare Messungen:'):
                current_section = 'measurements'
                continue
            elif stripped.startswith(('⏱', '✓', '⌛', '📋')):
                current_section = None
                continue

            # Parse input entries: "• Key: Value"
            if current_section == 'inputs' and stripped.startswith('•'):
                entry = stripped.lstrip('• ').strip()
                if ':' in entry:
                    key, _, value = entry.partition(':')
                    current_step['inputs'][key.strip()] = value.strip()

            # Parse notes (free text lines)
            if current_section == 'notes' and stripped:
                if current_step['notes']:
                    current_step['notes'] += ' '
                current_step['notes'] += stripped

        # Don't forget last step
        if current_step:
            steps.append(current_step)

        return steps

    def get_randomization(self) -> Optional[str]:
        """Get randomization order ('CON first' or 'EXZ first')."""
        if not self._protocol:
            self.parse()
        if not self._protocol:
            return None
        return self._protocol['metadata'].get('randomization')

    def get_metadata(self) -> Dict:
        """Get all protocol metadata."""
        if not self._protocol:
            self.parse()
        if not self._protocol:
            return {{}}
        return self._protocol['metadata']

    def get_step_by_id(self, step_id: str) -> Optional[Dict]:
        """Get a specific protocol step by its step_id (e.g. 'ft_block1')."""
        if not self._protocol:
            self.parse()
        if not self._protocol:
            return None
        for step in self._protocol['steps']:
            if step['step_id'] == step_id:
                return step
        return None

    def get_condition_map(self) -> Dict[str, Dict]:
        """
        Get Block -> condition mapping based on the randomization order.

        Returns dict like:
            {{'Block1': {{'condition': 'Baseline', 'training_mode': None}}, ...}}
        """
        randomization = self.get_randomization()
        if randomization and randomization in self.CONDITION_MAPS:
            return self.CONDITION_MAPS[randomization]
        print(f"Warning: Unknown randomization '{{randomization}}', defaulting to 'CON first'")
        return self.CONDITION_MAPS['CON first']

    def create_condition_groups(self, emgfile_groups: Dict) -> Dict:
        """
        Create condition-based meta-grouping from emgfile groups.

        Takes the output of create_emgfile_groups() and maps each group to an
        experimental condition based on the Block number in the file basename
        and the randomization order from the protocol.

        Args:
            emgfile_groups: Output dict from create_emgfile_groups()

        Returns:
            Dict with structure:
                {{
                    'conditions': {{
                        'Baseline': {{
                            'block': 'Block1',
                            'training_mode': None,
                            'tracking_types': {{
                                'Trapezoid': [group1, ...],
                                'Pyramid': [group2, ...]
                            }}
                        }},
                        ...
                    }},
                    'unmatched': [...],
                    'randomization': str,
                    'metadata': dict
                }}
        """
        condition_map = self.get_condition_map()

        # Initialize condition groups in defined order
        conditions = {{}}
        for block_key, info in condition_map.items():
            cond = info['condition']
            if cond not in conditions:
                conditions[cond] = {{
                    'block': block_key,
                    'training_mode': info['training_mode'],
                    'tracking_types': {{}},
                }}

        unmatched = []

        # Process multi-file groups
        for group in emgfile_groups.get('groups', []):
            self._assign_group(group, group.get('file_base', ''), condition_map, conditions, unmatched)

        # Process ungrouped (single) files as individual groups
        for entry in emgfile_groups.get('ungrouped', []):
            filename = entry.get('filename', '')
            single_group = {{
                'name': filename,
                'muscle': self._extract_muscle(filename),
                'file_base': self._extract_basename(filename),
                'files': [entry],
                'count': 1,
                'concatenated': entry.get('data'),
            }}
            self._assign_group(single_group, single_group['file_base'], condition_map, conditions, unmatched)

        return {{
            'conditions': conditions,
            'unmatched': unmatched,
            'randomization': self.get_randomization(),
            'metadata': self.get_metadata(),
        }}

    def _assign_group(self, group, file_base, condition_map, conditions, unmatched):
        """Assign a single group to a condition based on its Block number."""
        block_match = re.search(r'Block(\\d+)', file_base)
        if not block_match:
            unmatched.append(group)
            return

        block_str = f"Block{{block_match.group(1)}}"
        block_info = condition_map.get(block_str)
        if not block_info:
            unmatched.append(group)
            return

        condition = block_info['condition']

        # Determine tracking type from file_base
        # Filenames use 'Trap' (short) or 'Trapezoid' (full) for trapezoid tracking
        tracking_type = 'Other'
        if 'Trap' in file_base:
            tracking_type = 'Trapezoid'
        elif 'Pyramid' in file_base:
            tracking_type = 'Pyramid'

        if tracking_type not in conditions[condition]['tracking_types']:
            conditions[condition]['tracking_types'][tracking_type] = []
        conditions[condition]['tracking_types'][tracking_type].append(group)

    @staticmethod
    def _extract_basename(filename: str) -> str:
        """Extract file basename (mirrors decomposition_export logic)."""
        name = os.path.basename(filename)
