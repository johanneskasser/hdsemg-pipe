class WorkfolderPaths:
    """Easy access to all workfolder paths and files."""

    def __init__(self, workfolder: str):
        self.workfolder = Path(workfolder)
        self.original_files = self.workfolder / "original_files"
        self.associated_grids = self.workfolder / "associated_grids"
        self.line_noise_cleaned = self.workfolder / "line_noise_cleaned"
        self.cropped_signal = self.workfolder / "cropped_signal"
        self.channelselection = self.workfolder / "channelselection"
        self.decomposition_auto = self.workfolder / "decomposition_auto"
        self.decomposition_results = self.workfolder / "decomposition_results"
        self.analysis = self.workfolder / "analysis"

    def list_original_files(self) -> List[Path]:
        """List original imported files."""
        if not self.original_files.exists():
            return []
        return sorted(self.original_files.glob("*.mat"))

    def list_final_json_files(self) -> List[Path]:
        """List final cleaned JSON files."""
        if not self.decomposition_results.exists():
            return []
        return sorted(self.decomposition_results.glob("*_cleaned.json"))

    def list_decomposition_files(self) -> List[Path]:
        """List decomposition JSON files (before cleaning)."""
        if not self.decomposition_auto.exists():
            return []
        # Exclude metadata files
        exclude = ['decomposition_mapping.json', 'multigrid_groupings.json',
                   'covisi_pre_filter_report.json', 'covisi_post_validation_report.json']
        return sorted([f for f in self.decomposition_auto.glob("*.json")
                      if f.name not in exclude])

    def get_metadata_files(self) -> Dict[str, Path]:
        """Get paths to all metadata JSON files."""
        return {{
            'decomposition_mapping': self.decomposition_auto / "decomposition_mapping.json",
            'multigrid_groupings': self.decomposition_auto / "multigrid_groupings.json",
            'covisi_pre_filter': self.decomposition_auto / "covisi_pre_filter_report.json",
            'covisi_post_validation': self.decomposition_auto / "covisi_post_validation_report.json"
        }}

    def list_analysis_files(self) -> Dict[str, List[Path]]:
        """List analysis output files (RMS, CoVISI, etc.)."""
        if not self.analysis.exists():
            return {{}}

        return {{
            'summary_plots': sorted(self.analysis.glob("*_summary.png")),
            'channel_plots': sorted(self.analysis.glob("*_per_channel.png")),
            'reports': sorted(self.analysis.glob("*_report.csv")),
            'summaries': sorted(self.analysis.glob("*_summary.txt"))
        }}

    def find_protocol_file(self) -> Optional[Path]:
        """Find the hdsemg-trail protocol file (*_protokoll.txt) in the workfolder root."""
        protocol_files = sorted(self.workfolder.glob("*_protokoll.txt"))
        if protocol_files:
            return protocol_files[0]
        return None


class MetadataReader:
    """Read and parse pipeline metadata."""

    def __init__(self, paths: WorkfolderPaths):
        self.paths = paths

    def _read_json(self, path: Path) -> Optional[Dict]:
        """Helper to read JSON file."""
        if not path.exists():
            return None
        try:
            with open(path, 'r', encoding='utf-8') as f:
                return json.load(f)
        except Exception as e:
            print(f"Warning: Failed to read {{path.name}}: {{e}}")
            return None

    def get_decomposition_mapping(self) -> Optional[Dict]:
        """Read decomposition file mapping."""
        path = self.paths.get_metadata_files()['decomposition_mapping']
        return self._read_json(path)

    def get_multigrid_groupings(self) -> Optional[Dict]:
        """Read multi-grid grouping configuration."""
        path = self.paths.get_metadata_files()['multigrid_groupings']
        return self._read_json(path)

    def get_covisi_pre_filter(self) -> Optional[Dict]:
        """Read CoVISI pre-filter report."""
        path = self.paths.get_metadata_files()['covisi_pre_filter']
        return self._read_json(path)

    def get_covisi_post_validation(self) -> Optional[Dict]:
        """Read CoVISI post-validation report."""
        path = self.paths.get_metadata_files()['covisi_post_validation']
        return self._read_json(path)

    def get_rms_quality_data(self, json_file: Path) -> Optional[Dict]:
        """Read RMS quality data for a specific file."""
        # RMS JSON files are stored in cropped_signal folder
        base_name = json_file.stem.replace('_cleaned', '')
        rms_json = self.paths.cropped_signal / f"{{base_name}}_rms.json"
        return self._read_json(rms_json)

    def check_skip_marker(self, folder: Path) -> bool:
        """Check if a step was skipped."""
        skip_marker = folder / ".skip_marker.json"
        return skip_marker.exists()


